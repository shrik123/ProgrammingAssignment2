## Matrix inversion is quite inefficient so it may be more efficient to cache 
## the inverse rather than continuously resolving for them.
## The functions below create a "matrix" object that
## stores a matrix and caches its inverse.

## This function creates "matrix" object capable of caching its inverse.

makeCacheMatrix <- function(x = matrix()) {
        inv <- NULL
        set <- function(y) {
                x <<- y
                inv <<- NULL
        }
        get <- function() x
        setInverse <- function(inverse) inv <<- inverse
        getInverse <- function() inv
        list(set = set,
             get = get,
             setInverse = setInverse,
             getInverse = getInverse)
}


## This function computes the inverse of the"matrix" created by the above function.
## If the inverse has already been calculated, the cached inverse will be retrieved.

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        inv <- x$getInverse()
        if (!is.null(inv)) {
                message("getting cached data")
                return(inv)
        }
        mat <- x$get()
        inv <- solve(mat, ...)
        x$setInverse(inv)
        inv
}
